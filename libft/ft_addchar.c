#include "libft.h"

char	*ft_addchar(char *str, char c)
{
	char	*s;
	int		i;

	s = malloc(ft_strlen(str) + 2);
	i = 0;
	if (str != 0)
	{
		while (str[i] != '\0')
		{
			s[i] = str[i];
			i++;
		}
		s[i] = c;
	}
	s[i + 1] = '\0';
	return (s);
}
